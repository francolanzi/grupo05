    private void PBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PBuscarActionPerformed
        DefaultTableModel model = (DefaultTableModel) tablaProfesores.getModel();
        model.setRowCount(0);
        Iterator<Profesor> profesores = Controlador.getInstance().ubicaProfesor(CampoParaApellidoProfesor.getText(),
                                                                                CampoParaNombreProfesor.getText());
        while (profesores.hasNext())
        {
            Profesor profesor = profesores.next();
            model.addRow(new Object[] {profesor.getId(), profesor.getNombre(), profesor.getApellido(),
                         profesor.getDomicilio().getCalle(), profesor.getDomicilio().getNumero(),
                         profesor.getTelefono(), profesor.getEmail()});
        }
    }//GEN-LAST:event_PBuscarActionPerformed

